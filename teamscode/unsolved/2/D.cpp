int phash(string const &S, int a, int m)
{
	long long ans = 0;
	for (char c : S)
		ans = (ans * a + c) % m;
	return (int)ans;
}
vector<int> phashes(vector<string> const &S, int a, int m)
{
	vector<int> H(size(S));
	for (int i = 0; i < (int)size(S); i++)
		H[i] = phash(S[i], a, m);
	return H;
}
